//! Forward Error Correction (FEC) and video packet framing.
//!
//! This module defines the wire format for RustDesk's UDP video transport
//! and provides fragmentation/reassembly of video frames into MTU-sized
//! packets with FEC headers.
//!
//! # Wire Format
//!
//! Each UDP datagram carries a 24-byte [`VideoPacketHeader`] followed by
//! payload bytes. The header is serialized in little-endian byte order
//! using `#[repr(C, packed)]` for zero-copy compatibility.
//!
//! ```text
//! +------------------+---------------------------+
//! | VideoPacketHeader |       Payload             |
//! |    (24 bytes)     |   (up to ~1376 bytes)     |
//! +------------------+---------------------------+
//! ```
//!
//! # FEC Strategy
//!
//! XOR-based forward error correction: for every N data packets in a group,
//! one parity packet is generated by XOR-ing all data payloads together
//! (shorter payloads are zero-padded to the length of the longest).
//!
//! This allows recovery of any single lost data packet per FEC group.
//! If 2 or more data packets are lost, recovery is not possible.
//!
//! # Frame Fragmentation
//!
//! Encoded video frames (H.264/H.265 NALUs) are split into fragments that
//! fit within the transport's MTU. Each fragment gets its own header with
//! `fragment_index` and `fragment_count` fields for reassembly.

use std::collections::HashMap;

/// Maximum payload bytes per packet. Assumes ~1400 byte MTU minus the
/// 24-byte header.
pub const MAX_PAYLOAD_SIZE: usize = 1376;

/// Size of the [`VideoPacketHeader`] in bytes. Verified by compile-time
/// and runtime tests.
pub const HEADER_SIZE: usize = 24;

/// Frame type: predictive frame (depends on previous frames).
pub const FRAME_TYPE_P: u8 = 0;

/// Frame type: intra/key frame (self-contained, no dependencies).
pub const FRAME_TYPE_I: u8 = 1;

/// Frame type: FEC parity packet (XOR of all data packets in the group).
pub const FRAME_TYPE_PARITY: u8 = 2;

/// Video packet header for the UDP transport.
///
/// This 24-byte header prefixes every video datagram. It carries enough
/// metadata for the receiver to:
/// - Detect packet loss (via `sequence` gaps)
/// - Reassemble fragmented frames (`fragment_index`, `fragment_count`)
/// - Apply FEC recovery (`fec_group_id`, `fec_data_count`, `fec_parity_count`)
/// - Calculate jitter (`timestamp_ms`)
/// - Prioritize keyframes (`frame_type`)
///
/// # Wire Layout
///
/// All fields are little-endian. The struct uses `#[repr(C, packed)]` to
/// guarantee the layout matches the wire format exactly.
///
/// | Offset | Size | Field             |
/// |--------|------|-------------------|
/// | 0      | 4    | sequence          |
/// | 4      | 4    | frame_number      |
/// | 8      | 2    | fragment_index    |
/// | 10     | 2    | fragment_count    |
/// | 12     | 1    | frame_type        |
/// | 13     | 1    | fec_group_id      |
/// | 14     | 1    | fec_data_count    |
/// | 15     | 1    | fec_parity_count  |
/// | 16     | 4    | timestamp_ms      |
/// | 20     | 4    | payload_length    |
// Note: `#[repr(C)]` (not `packed`) is used because the field order is
// already naturally aligned — no padding is inserted, and the struct is
// exactly 24 bytes. This avoids undefined behavior from creating references
// to unaligned fields while maintaining wire-format compatibility.
// The `header_size_is_24_bytes` test verifies this at compile time.
#[repr(C)]
#[derive(Debug, Clone, Copy)]
pub struct VideoPacketHeader {
    /// Per-packet monotonic sequence number. Wraps at `u32::MAX`.
    /// Used by the receiver to detect gaps (packet loss).
    pub sequence: u32,

    /// Per-frame monotonic counter. All fragments of the same frame share
    /// the same `frame_number`.
    pub frame_number: u32,

    /// Zero-based index of this fragment within the frame.
    /// Range: `0..fragment_count`.
    pub fragment_index: u16,

    /// Total number of data fragments in this frame (excludes FEC parity
    /// packets).
    pub fragment_count: u16,

    /// Frame type: [`FRAME_TYPE_P`] (0) or [`FRAME_TYPE_I`] (1).
    pub frame_type: u8,

    /// Identifies which FEC group this packet belongs to. All data and
    /// parity packets in the same group share this ID.
    pub fec_group_id: u8,

    /// Number of data packets in this FEC group.
    pub fec_data_count: u8,

    /// Number of parity packets in this FEC group.
    pub fec_parity_count: u8,

    /// Sender timestamp in milliseconds since an arbitrary epoch.
    /// Used for jitter estimation and one-way delay calculation.
    pub timestamp_ms: u32,

    /// Number of payload bytes following this header.
    pub payload_length: u32,
}

impl VideoPacketHeader {
    /// Serialize this header to a 24-byte little-endian byte array.
    pub fn to_bytes(&self) -> [u8; HEADER_SIZE] {
        let mut buf = [0u8; HEADER_SIZE];
        buf[0..4].copy_from_slice(&self.sequence.to_le_bytes());
        buf[4..8].copy_from_slice(&self.frame_number.to_le_bytes());
        buf[8..10].copy_from_slice(&self.fragment_index.to_le_bytes());
        buf[10..12].copy_from_slice(&self.fragment_count.to_le_bytes());
        buf[12] = self.frame_type;
        buf[13] = self.fec_group_id;
        buf[14] = self.fec_data_count;
        buf[15] = self.fec_parity_count;
        buf[16..20].copy_from_slice(&self.timestamp_ms.to_le_bytes());
        buf[20..24].copy_from_slice(&self.payload_length.to_le_bytes());
        buf
    }

    /// Deserialize a header from a 24-byte little-endian byte slice.
    ///
    /// # Errors
    ///
    /// Returns `None` if `data` is shorter than [`HEADER_SIZE`] bytes.
    pub fn from_bytes(data: &[u8]) -> Option<Self> {
        if data.len() < HEADER_SIZE {
            return None;
        }
        Some(Self {
            sequence: u32::from_le_bytes([data[0], data[1], data[2], data[3]]),
            frame_number: u32::from_le_bytes([data[4], data[5], data[6], data[7]]),
            fragment_index: u16::from_le_bytes([data[8], data[9]]),
            fragment_count: u16::from_le_bytes([data[10], data[11]]),
            frame_type: data[12],
            fec_group_id: data[13],
            fec_data_count: data[14],
            fec_parity_count: data[15],
            timestamp_ms: u32::from_le_bytes([data[16], data[17], data[18], data[19]]),
            payload_length: u32::from_le_bytes([data[20], data[21], data[22], data[23]]),
        })
    }
}

impl PartialEq for VideoPacketHeader {
    fn eq(&self, other: &Self) -> bool {
        self.sequence == other.sequence
            && self.frame_number == other.frame_number
            && self.fragment_index == other.fragment_index
            && self.fragment_count == other.fragment_count
            && self.frame_type == other.frame_type
            && self.fec_group_id == other.fec_group_id
            && self.fec_data_count == other.fec_data_count
            && self.fec_parity_count == other.fec_parity_count
            && self.timestamp_ms == other.timestamp_ms
            && self.payload_length == other.payload_length
    }
}

impl Eq for VideoPacketHeader {}

/// A complete packet: header + payload, ready to send as a single datagram.
#[derive(Debug, Clone)]
pub struct VideoPacket {
    pub header: VideoPacketHeader,
    pub payload: Vec<u8>,
}

impl VideoPacket {
    /// Serialize this packet (header + payload) into a byte vector suitable
    /// for sending as a single UDP datagram.
    pub fn to_bytes(&self) -> Vec<u8> {
        let mut buf = Vec::with_capacity(HEADER_SIZE + self.payload.len());
        buf.extend_from_slice(&self.header.to_bytes());
        buf.extend_from_slice(&self.payload);
        buf
    }

    /// Deserialize a packet from raw datagram bytes.
    ///
    /// Returns `None` if the data is too short for a header or if
    /// `payload_length` exceeds the available data.
    pub fn from_bytes(data: &[u8]) -> Option<Self> {
        let header = VideoPacketHeader::from_bytes(data)?;
        let payload_len = header.payload_length as usize;
        if data.len() < HEADER_SIZE + payload_len {
            return None;
        }
        let payload = data[HEADER_SIZE..HEADER_SIZE + payload_len].to_vec();
        Some(Self { header, payload })
    }
}

/// Result of fragmenting a video frame.
pub struct FragmentedFrame {
    /// The individual packets (header + payload) ready for transmission.
    pub packets: Vec<VideoPacket>,
}

/// Fragment a video frame into MTU-sized packets with headers.
///
/// Each fragment gets a [`VideoPacketHeader`] with the correct `fragment_index`
/// and `fragment_count`. The `sequence` number is assigned monotonically
/// starting from `start_sequence`.
///
/// # Arguments
///
/// * `frame_data` — The encoded video frame (e.g., H.265 NALUs).
/// * `frame_number` — Monotonic frame counter.
/// * `frame_type` — [`FRAME_TYPE_I`] or [`FRAME_TYPE_P`].
/// * `timestamp_ms` — Sender timestamp in milliseconds.
/// * `start_sequence` — Sequence number for the first fragment. Subsequent
///   fragments get `start_sequence + 1`, etc.
/// * `fec_group_id` — FEC group identifier for this frame's fragments.
///
/// # Returns
///
/// A [`FragmentedFrame`] containing one packet per fragment. An empty
/// `frame_data` produces a single packet with zero-length payload.
pub fn fragment_frame(
    frame_data: &[u8],
    frame_number: u32,
    frame_type: u8,
    timestamp_ms: u32,
    start_sequence: u32,
    fec_group_id: u8,
) -> FragmentedFrame {
    let chunks: Vec<&[u8]> = if frame_data.is_empty() {
        vec![&[]]
    } else {
        frame_data.chunks(MAX_PAYLOAD_SIZE).collect()
    };
    let fragment_count = chunks.len() as u16;

    let packets = chunks
        .iter()
        .enumerate()
        .map(|(i, chunk)| {
            let header = VideoPacketHeader {
                sequence: start_sequence.wrapping_add(i as u32),
                frame_number,
                fragment_index: i as u16,
                fragment_count,
                frame_type,
                fec_group_id,
                fec_data_count: fragment_count as u8,
                fec_parity_count: 0, // Set by FecEncoder when parity is added
                timestamp_ms,
                payload_length: chunk.len() as u32,
            };
            VideoPacket {
                header,
                payload: chunk.to_vec(),
            }
        })
        .collect();

    FragmentedFrame { packets }
}

/// Fragment a video frame and append FEC parity packets.
///
/// This is a convenience wrapper around [`fragment_frame`] that runs the
/// resulting packets through an [`FecEncoder`] to generate XOR parity.
///
/// # Arguments
///
/// Same as [`fragment_frame`], plus:
/// * `encoder` — The [`FecEncoder`] to use. If `None`, no parity is added.
///
/// # Returns
///
/// A [`FragmentedFrame`] containing data packets followed by parity packet(s).
pub fn fragment_frame_with_fec(
    frame_data: &[u8],
    frame_number: u32,
    frame_type: u8,
    timestamp_ms: u32,
    start_sequence: u32,
    encoder: Option<&mut FecEncoder>,
) -> FragmentedFrame {
    // Use encoder's next_group_id if available, otherwise 0.
    let fec_group_id = encoder
        .as_ref()
        .map(|e| e.next_group_id)
        .unwrap_or(0);

    let fragmented = fragment_frame(
        frame_data,
        frame_number,
        frame_type,
        timestamp_ms,
        start_sequence,
        fec_group_id,
    );

    match encoder {
        Some(enc) => FragmentedFrame {
            packets: enc.encode(fragmented.packets),
        },
        None => fragmented,
    }
}

/// Reassembly buffer for collecting fragments of a single frame.
struct FrameAssembly {
    /// Expected number of fragments.
    fragment_count: u16,
    /// Received fragments indexed by fragment_index.
    fragments: HashMap<u16, Vec<u8>>,
    /// Total payload bytes received so far.
    total_bytes: usize,
}

impl FrameAssembly {
    fn new(fragment_count: u16) -> Self {
        Self {
            fragment_count,
            fragments: HashMap::with_capacity(fragment_count as usize),
            total_bytes: 0,
        }
    }

    /// Insert a fragment. Returns `true` if the frame is now complete.
    fn insert(&mut self, index: u16, payload: Vec<u8>) -> bool {
        if index >= self.fragment_count {
            return false;
        }
        self.total_bytes += payload.len();
        self.fragments.insert(index, payload);
        self.fragments.len() == self.fragment_count as usize
    }

    /// Assemble the complete frame from collected fragments.
    /// Returns `None` if not all fragments have been received.
    fn assemble(&self) -> Option<Vec<u8>> {
        if self.fragments.len() != self.fragment_count as usize {
            return None;
        }
        let mut frame = Vec::with_capacity(self.total_bytes);
        for i in 0..self.fragment_count {
            let fragment = self.fragments.get(&i)?;
            frame.extend_from_slice(fragment);
        }
        Some(frame)
    }
}

/// Reassembles fragmented video packets back into complete frames.
///
/// Maintains per-frame assembly buffers keyed by `frame_number`. Once all
/// fragments for a frame arrive, the frame data can be retrieved via
/// [`reassemble`](Self::reassemble).
///
/// Old incomplete frames are automatically purged when the frame number
/// advances beyond the retention window.
pub struct FrameReassembler {
    /// In-progress frame assemblies, keyed by frame_number.
    assemblies: HashMap<u32, FrameAssembly>,
    /// The highest frame_number that has been fully reassembled.
    last_completed_frame: Option<u32>,
    /// Maximum number of in-progress assemblies before pruning old ones.
    max_pending_frames: usize,
}

impl FrameReassembler {
    /// Create a new reassembler.
    ///
    /// `max_pending_frames` controls how many incomplete frames are kept
    /// in memory before the oldest are discarded. A reasonable default is
    /// 16 (about 0.5 seconds at 30fps).
    pub fn new(max_pending_frames: usize) -> Self {
        Self {
            assemblies: HashMap::new(),
            last_completed_frame: None,
            max_pending_frames,
        }
    }

    /// Feed a received packet into the reassembler.
    ///
    /// Returns `Some(frame_data)` if this packet completed a frame.
    /// Returns `None` if more fragments are needed.
    pub fn reassemble(&mut self, packet: &VideoPacket) -> Option<Vec<u8>> {
        let frame_number = packet.header.frame_number;

        // Discard packets for frames older than the last completed one.
        if let Some(last) = self.last_completed_frame {
            if frame_number <= last {
                return None;
            }
        }

        // Prune old assemblies if we have too many pending.
        if self.assemblies.len() >= self.max_pending_frames
            && !self.assemblies.contains_key(&frame_number)
        {
            // Remove the oldest frame_number.
            if let Some(&oldest) = self.assemblies.keys().min() {
                self.assemblies.remove(&oldest);
            }
        }

        let assembly = self
            .assemblies
            .entry(frame_number)
            .or_insert_with(|| FrameAssembly::new(packet.header.fragment_count));

        let complete =
            assembly.insert(packet.header.fragment_index, packet.payload.clone());

        if complete {
            let result = assembly.assemble();
            self.assemblies.remove(&frame_number);
            self.last_completed_frame = Some(frame_number);
            result
        } else {
            None
        }
    }

    /// Returns the number of frames currently being assembled.
    pub fn pending_frame_count(&self) -> usize {
        self.assemblies.len()
    }

    /// Returns the last completed frame number, if any.
    pub fn last_completed_frame(&self) -> Option<u32> {
        self.last_completed_frame
    }
}

/// Reed-Solomon FEC encoder.
///
/// Takes a set of data packets (fragments of a video frame) and produces
/// additional parity packets. Any K of (K + R) packets suffice to recover
/// the original K data packets.
///
/// # Current Status
///
/// Stubbed — the actual Reed-Solomon implementation requires adding
/// `reed-solomon-erasure` (or `reed-solomon-simd`) to `Cargo.toml`.
pub struct FecEncoder {
    /// FEC overhead ratio. 0.1 = 10% parity packets.
    fec_ratio: f32,
    /// Next FEC group ID to assign.
    next_group_id: u8,
}

impl FecEncoder {
    /// Create a new FEC encoder with the given parity ratio.
    ///
    /// `fec_ratio` controls the amount of redundancy:
    /// - 0.1 = 10% overhead (1 parity per 10 data packets)
    /// - 0.2 = 20% overhead (recommended for keyframes)
    /// - 0.3 = 30% overhead (aggressive, for very lossy networks)
    pub fn new(fec_ratio: f32) -> Self {
        Self {
            fec_ratio: fec_ratio.clamp(0.0, 1.0),
            next_group_id: 0,
        }
    }

    /// Get the current FEC ratio.
    pub fn fec_ratio(&self) -> f32 {
        self.fec_ratio
    }

    /// Update the FEC ratio adaptively based on observed packet loss.
    pub fn set_fec_ratio(&mut self, ratio: f32) {
        self.fec_ratio = ratio.clamp(0.0, 1.0);
    }

    /// Generate a single XOR parity packet from a slice of data packets.
    ///
    /// All payloads are XOR'd together byte-by-byte, with shorter payloads
    /// zero-padded to match the longest. The resulting parity payload can
    /// recover any single missing data packet via XOR with the remaining
    /// data packets.
    ///
    /// # Arguments
    ///
    /// * `data_packets` — The data packets in this FEC group.
    /// * `group_id` — FEC group identifier.
    /// * `next_sequence` — Sequence number for the parity packet.
    ///
    /// # Returns
    ///
    /// A [`VideoPacket`] with `frame_type = FRAME_TYPE_PARITY` (2) containing
    /// the XOR parity payload, or `None` if `data_packets` is empty.
    pub fn generate_parity(
        data_packets: &[VideoPacket],
        group_id: u8,
        next_sequence: u32,
    ) -> Option<VideoPacket> {
        if data_packets.is_empty() {
            return None;
        }

        // Find the maximum payload length for zero-padding.
        let max_len = data_packets
            .iter()
            .map(|p| p.payload.len())
            .max()
            .unwrap_or(0);

        // XOR all payloads together, padding shorter ones with zeros.
        let mut parity = vec![0u8; max_len];
        for packet in data_packets {
            for (i, &byte) in packet.payload.iter().enumerate() {
                parity[i] ^= byte;
            }
            // Bytes beyond packet.payload.len() are implicitly XOR'd with 0
            // (no-op), so no explicit padding needed.
        }

        let data_count = data_packets.len() as u8;
        let header = VideoPacketHeader {
            sequence: next_sequence,
            frame_number: data_packets[0].header.frame_number,
            fragment_index: data_count as u16, // parity follows data fragments
            fragment_count: data_packets[0].header.fragment_count,
            frame_type: FRAME_TYPE_PARITY,
            fec_group_id: group_id,
            fec_data_count: data_count,
            fec_parity_count: 1,
            timestamp_ms: data_packets[0].header.timestamp_ms,
            payload_length: parity.len() as u32,
        };

        Some(VideoPacket {
            header,
            payload: parity,
        })
    }

    /// Encode a set of data packets, producing an additional XOR parity packet.
    ///
    /// The input `packets` are the data fragments of a single video frame
    /// (as produced by [`fragment_frame`]). This method:
    ///
    /// 1. Assigns an FEC group ID to all packets.
    /// 2. Generates 1 XOR parity packet (if `fec_ratio > 0` and there are
    ///    data packets).
    /// 3. Updates `fec_data_count` and `fec_parity_count` in all headers.
    /// 4. Returns the combined data + parity packets.
    pub fn encode(&mut self, mut packets: Vec<VideoPacket>) -> Vec<VideoPacket> {
        if packets.is_empty() {
            return packets;
        }

        let group_id = self.next_group_id;
        self.next_group_id = self.next_group_id.wrapping_add(1);

        let data_count = packets.len() as u8;

        // With XOR FEC we can generate at most 1 parity packet.
        // Generate it if the ratio indicates any parity is desired.
        let parity_count = if self.fec_ratio > 0.0 { 1u8 } else { 0u8 };

        // Update all data packet headers with FEC group info.
        for packet in &mut packets {
            packet.header.fec_group_id = group_id;
            packet.header.fec_data_count = data_count;
            packet.header.fec_parity_count = parity_count;
        }

        // Generate XOR parity packet if requested.
        if parity_count > 0 {
            let next_seq = packets
                .last()
                .unwrap()
                .header
                .sequence
                .wrapping_add(1);
            if let Some(parity_packet) =
                Self::generate_parity(&packets, group_id, next_seq)
            {
                packets.push(parity_packet);
            }
        }

        packets
    }
}

/// XOR-based FEC decoder.
///
/// Given the data packets and optional parity packet from an FEC group,
/// reconstructs a single missing data packet using XOR recovery.
///
/// # Recovery Capability
///
/// - All data packets present: returns them directly (no recovery needed).
/// - Exactly 1 data packet missing + parity present: recovers via XOR.
/// - 2+ data packets missing: returns `None` (XOR FEC cannot recover).
pub struct FecDecoder;

impl FecDecoder {
    /// Create a new FEC decoder.
    pub fn new() -> Self {
        Self
    }

    /// Attempt to recover missing data packets using XOR parity.
    ///
    /// # Arguments
    ///
    /// * `received` — Slots for the FEC group: indices `0..data_count` are
    ///   data packet payloads, and index `data_count` (if present) is the
    ///   parity payload. `None` entries indicate missing packets.
    /// * `data_count` — Number of data packets in the group.
    /// * `_parity_count` — Number of parity packets (expected to be 0 or 1
    ///   for XOR FEC).
    ///
    /// # Returns
    ///
    /// `Some(data_payloads)` — a vector of `data_count` payloads in order
    /// if all data is available or recovery succeeded. `None` if recovery
    /// is impossible (2+ missing data packets, or 1 missing without parity).
    pub fn decode(
        &self,
        received: &[Option<Vec<u8>>],
        data_count: usize,
        _parity_count: usize,
    ) -> Option<Vec<Vec<u8>>> {
        if data_count == 0 {
            return Some(vec![]);
        }

        // Separate data slots from parity slot.
        let data_slots = &received[..data_count.min(received.len())];
        let parity_slot = received.get(data_count).and_then(|s| s.as_ref());

        // Count missing data packets.
        let mut missing_index = None;
        let mut missing_count = 0;
        for (i, slot) in data_slots.iter().enumerate() {
            if slot.is_none() {
                missing_count += 1;
                missing_index = Some(i);
            }
        }

        match missing_count {
            0 => {
                // All data packets present — just return them.
                let payloads = data_slots
                    .iter()
                    .map(|s| s.as_ref().unwrap().clone())
                    .collect();
                Some(payloads)
            }
            1 => {
                // Exactly 1 missing — recover using XOR parity.
                let parity = parity_slot?;
                let missing_idx = missing_index.unwrap();

                // XOR parity with all present data packets to recover
                // the missing one.
                let mut recovered = parity.clone();
                for (i, slot) in data_slots.iter().enumerate() {
                    if i == missing_idx {
                        continue;
                    }
                    let payload = slot.as_ref().unwrap();
                    // XOR each byte; if recovered is shorter than payload,
                    // extend it; if payload is shorter, only XOR up to its
                    // length.
                    if recovered.len() < payload.len() {
                        recovered.resize(payload.len(), 0);
                    }
                    for (j, &byte) in payload.iter().enumerate() {
                        recovered[j] ^= byte;
                    }
                }

                // Build the result with the recovered packet in place.
                let mut payloads: Vec<Vec<u8>> = Vec::with_capacity(data_count);
                for (i, slot) in data_slots.iter().enumerate() {
                    if i == missing_idx {
                        payloads.push(recovered.clone());
                    } else {
                        payloads.push(slot.as_ref().unwrap().clone());
                    }
                }
                Some(payloads)
            }
            _ => {
                // 2+ missing — XOR FEC cannot recover.
                None
            }
        }
    }

    /// Higher-level recovery: given a set of received [`VideoPacket`]s for
    /// a single FEC group, attempt to produce the complete set of data
    /// payloads in fragment order.
    ///
    /// This method arranges received packets into the slot array expected
    /// by [`decode`](Self::decode) and handles parity packet identification
    /// via `frame_type == FRAME_TYPE_PARITY`.
    ///
    /// # Returns
    ///
    /// `Some(data_payloads)` in fragment order if all data is present or
    /// recovery succeeded. `None` otherwise.
    pub fn try_recover(
        &self,
        packets: &[VideoPacket],
        data_count: usize,
    ) -> Option<Vec<Vec<u8>>> {
        if data_count == 0 {
            return Some(vec![]);
        }

        // Build slot array: data_count data slots + 1 parity slot.
        let mut slots: Vec<Option<Vec<u8>>> = vec![None; data_count + 1];

        for packet in packets {
            if packet.header.frame_type == FRAME_TYPE_PARITY {
                // Parity packet goes in the last slot.
                slots[data_count] = Some(packet.payload.clone());
            } else {
                let idx = packet.header.fragment_index as usize;
                if idx < data_count {
                    slots[idx] = Some(packet.payload.clone());
                }
            }
        }

        self.decode(&slots, data_count, 1)
    }
}

impl Default for FecDecoder {
    fn default() -> Self {
        Self::new()
    }
}

