    #[test]
    fn fec_round_trip_fragment_add_parity_drop_one_recover_reassemble() {
        // 1. Fragment a frame.
        let original_frame = b"The quick brown fox jumps over the lazy dog. \
            This is a test of the FEC round-trip: fragment, add parity, \
            drop one packet, recover, and reassemble.";

        let mut encoder = FecEncoder::new(0.5);
        let fragmented = fragment_frame_with_fec(
            original_frame,
            42,
            FRAME_TYPE_I,
            12345,
            0,
            Some(&mut encoder),
        );

        // Should have 1 data + 1 parity = 2 packets.
        assert_eq!(fragmented.packets.len(), 2);
        let data_count = fragmented.packets[0].header.fec_data_count as usize;
        assert_eq!(data_count, 1);

        // 2. Simulate dropping the data packet (keep only parity).
        // Actually, we need the parity to recover data. But with 1 data packet
        // the parity IS the data. Let's use a multi-fragment example.

        // Larger frame that produces multiple fragments.
        let large_frame: Vec<u8> = (0..255u8)
            .cycle()
            .take(MAX_PAYLOAD_SIZE * 4 + 100)
            .collect();

        let mut encoder2 = FecEncoder::new(0.5);
        let fragmented2 = fragment_frame_with_fec(
            &large_frame,
            100,
            FRAME_TYPE_I,
            99999,
            0,
            Some(&mut encoder2),
        );

        // 5 data packets + 1 parity = 6 total.
        assert_eq!(fragmented2.packets.len(), 6);
        let data_count2 = fragmented2.packets[0].header.fec_data_count as usize;
        assert_eq!(data_count2, 5);

        // 3. Drop fragment_index=2 (the third data packet).
        let received: Vec<VideoPacket> = fragmented2
            .packets
            .iter()
            .filter(|p| {
                p.header.fragment_index != 2 || p.header.frame_type == FRAME_TYPE_PARITY
            })
            .cloned()
            .collect();
        assert_eq!(received.len(), 5); // 4 data + 1 parity

        // 4. Recover using FEC decoder.
        let decoder = FecDecoder::new();
        let recovered = decoder.try_recover(&received, data_count2);
        assert!(recovered.is_some(), "Should recover with 1 missing packet");
        let payloads = recovered.unwrap();
        assert_eq!(payloads.len(), 5);

        // 5. Reassemble the frame from recovered payloads.
        let mut reassembled = Vec::new();
        for payload in &payloads {
            reassembled.extend_from_slice(payload);
        }

        // The reassembled data should match the original frame.
        // Note: the last fragment may have been padded in parity calculation,
        // but the payload itself is preserved as-is for present packets.
        // The recovered packet (fragment 2) was full-sized (MAX_PAYLOAD_SIZE)
        // so no padding issue.
        assert_eq!(
            &reassembled[..large_frame.len()],
            &large_frame[..],
            "Reassembled frame must match original"
        );
    }

    #[test]
    fn fec_round_trip_no_loss_reassemble() {
        // Fragment + FEC encode, receive all packets, reassemble without
        // needing recovery.
        let frame_data: Vec<u8> = (0..200u8).collect();

        let mut encoder = FecEncoder::new(0.5);
        let fragmented = fragment_frame_with_fec(
            &frame_data,
            1,
            FRAME_TYPE_P,
            0,
            0,
            Some(&mut encoder),
        );

        // 1 data + 1 parity.
        assert_eq!(fragmented.packets.len(), 2);

        // Feed only the data packet to the reassembler (skip parity).
        let mut reassembler = FrameReassembler::new(16);
        let data_packets: Vec<&VideoPacket> = fragmented
            .packets
            .iter()
            .filter(|p| p.header.frame_type != FRAME_TYPE_PARITY)
            .collect();

        let mut result = None;
        for packet in data_packets {
            result = reassembler.reassemble(packet);
        }
        assert_eq!(result, Some(frame_data));
    }

