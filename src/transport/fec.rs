        // All 3 fragments had identical payload (0xAB repeated).
        assert_eq!(payloads[0], vec![0xAB; MAX_PAYLOAD_SIZE]);
        assert_eq!(payloads[1], vec![0xAB; MAX_PAYLOAD_SIZE]);
        assert_eq!(payloads[2], vec![0xAB; MAX_PAYLOAD_SIZE]);
