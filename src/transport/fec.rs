        let mut encoder = FecEncoder::new(0.5);

        // Create 3 data packets.
        let frame = fragment_frame(
            &vec![0xAB; MAX_PAYLOAD_SIZE * 3],
            1, FRAME_TYPE_I, 0, 0, 0,
        );
        let packets = encoder.encode(frame.packets);
