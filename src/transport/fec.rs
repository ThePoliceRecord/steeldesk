    #[test]
    fn fec_decoder_recover_with_different_payload_sizes() {
        let decoder = FecDecoder::new();

        // Data: [0xAA, 0xBB] (2 bytes) and [0x11, 0x22, 0x33, 0x44] (4 bytes)
        // For parity, short payload is zero-padded: [0xAA, 0xBB, 0x00, 0x00]
        // Parity = [0xAA^0x11, 0xBB^0x22, 0x00^0x33, 0x00^0x44]
        //        = [0xBB, 0x99, 0x33, 0x44]
        let received: Vec<Option<Vec<u8>>> = vec![
            None,                              // packet 0 missing (was 2 bytes)
            Some(vec![0x11, 0x22, 0x33, 0x44]),// packet 1 present
            Some(vec![0xBB, 0x99, 0x33, 0x44]),// parity (4 bytes)
        ];
        let result = decoder.decode(&received, 2, 1);
        assert!(result.is_some());
        let payloads = result.unwrap();
        // Recovered payload will be 4 bytes (parity length), with trailing zeros
        // from the zero-padding. This is expected behavior — the recovered
        // payload includes the padding bytes.
        assert_eq!(payloads[0], vec![0xAA, 0xBB, 0x00, 0x00]);
    }

